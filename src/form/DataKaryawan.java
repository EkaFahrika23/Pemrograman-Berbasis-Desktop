    private void txt_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_tambahActionPerformed
        // TODO add your handling code here:
        
        //bagian masukkan data ke dalam database
        try {
            Connection con = KoneksiHelper.getConnection();
            //query insert data ke dalam database mysql
            pst = con.prepareStatement("INSERT INTO karyawan (id_karyawan, nama, usia, no_telp, alamat) VALUES(?,?,?,?,?)");
            //validasi ketika textField kosong

            if (txt_id_karyawan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Id karyawan tidak boleh kosong");
            } else if (txt_nama.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong");
            } else if (txt_usia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Usia tidak boleh kosong");
            } else if (txt_no_telp.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No_telp tidak boleh kosong");
            } else if (txt_alamat.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Alamat tidak boleh kosong");    
            } else {
                //digunakan untuk memasukkan data ke masing2 variabel textfield
                
                //misal bingung pst sama rs bisa diliat di variabel diatas
                pst.setString(1, txt_id_karyawan.getText());
                pst.setString(2, txt_nama.getText());
                pst.setString(3, txt_usia.getText());
                pst.setString(4, txt_no_telp.getText());
                pst.setString(5, txt_alamat.getText());
                pst.executeUpdate();
                
                
                //setelah nginput data kasih ini biar textfieldnya kosong lagi
                txt_id_karyawan.setText("");
                txt_nama.setText("");
                txt_usia.setText("");
                txt_no_telp.setText("");
                txt_alamat.setText("");
                
                //setelah berhasil
                JOptionPane.showMessageDialog(null, "Tambah Data Karyawan Berhasil");
            }

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());
            e.printStackTrace();
        }   
    }//GEN-LAST:event_txt_tambahActionPerformed

