    //penempatan dan pencocokan data tabel di database dan di tabel JTable
    private void fetchData() {
        ArrayList<DataKaryawan2> list = karyawan();
        DefaultTableModel model = (DefaultTableModel) tbl_karyawan.getModel();
        Object[] row = new Object[5];

        for (int i = 0; i < list.size(); i++) {

            row[0] = list.get(i).getId_karyawan();
            row[1] = list.get(i).getNama();
            row[2] = list.get(i).getUsia();
            row[3] = list.get(i).getNo_telp();
            row[4] = list.get(i).getAlamat();

            model.addRow(row);
        }
    }
    //perintah untuk memasukkan data ke dalam tabel dan select data
    public ArrayList<DataKaryawan2> karyawan() {
        ArrayList<DataKaryawan2> karyawan = new ArrayList<>();
        try {
            Connection con = KoneksiHelper.getConnection();
            String sql = "SELECT id_karyawan, nama, usia, no_telp, alamat FROM karyawan";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            DataKaryawan2 DataKaryawan;
            while (rs.next()) {
                DataKaryawan = new DataKaryawan2(rs.getString("id_karyawan"), rs.getString("nama"), rs.getInt("usia"), rs.getString ("no_telp"), rs.getString("alamat"));
                karyawan.add(DataKaryawan);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return karyawan;
    }

