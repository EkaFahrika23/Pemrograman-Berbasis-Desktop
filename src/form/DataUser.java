    public ArrayList<DataUser2> user() {
        ArrayList<DataUser2> user = new ArrayList<>();
        try {
            Connection con = KoneksiHelper.getConnection();
            String sql = "SELECT id_user, username, password, role FROM user";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            DataUser2 DataUser;
            while (rs.next()) {
                DataUser = new DataUser2(rs.getInt("id_user"), rs.getString("username"), rs.getString("password"), rs.getInt("role"));
                user.add(DataUser);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return user;
    }

