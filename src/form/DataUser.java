    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // memfungsikan button hapus
        try {

            int row = tbl_user.getSelectedRow();
            Connection con = KoneksiHelper.getConnection();
            String value = (tbl_user.getModel().getValueAt(row, 0).toString());
            String sql = "DELETE FROM user WHERE id_user = '" + value + "'";
            pst = con.prepareStatement(sql);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) tbl_user.getModel();
            model.setRowCount(0);
            fetchData();
            JOptionPane.showMessageDialog(null, "Data terhapus");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void txt_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_ubahActionPerformed
        // memfungsikan button ubah
        
        try {
            
            Connection con = KoneksiHelper.getConnection();
            int row = tbl_user.getSelectedRow();
            String value = (tbl_user.getModel().getValueAt(row, 0).toString());
            String sql = "UPDATE user SET id_user=?,username=?,password=?,role=? WHERE id_user = '" + value + "'";
            if (txt_id_user.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Id tidak boleh kosong");
            } else if (txt_username.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong");
            } else if (txt_password.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Passwordtidak boleh kosong");
            
            } else {
                pst = con.prepareStatement(sql);
                pst.setString(1, txt_id_user.getText());
                pst.setString(2, txt_username.getText());
                pst.setString(3, txt_password.getText());
                pst.setString(4, cb_role.getSelectedItem().toString());
                pst.executeUpdate();

                DefaultTableModel model = (DefaultTableModel) tbl_user.getModel();
                model.setRowCount(0);
                fetchData();
                txt_id_user.setText("");
                txt_username.setText("");
                txt_password.setText("");
                cb_role.setSelectedItem("");
                JOptionPane.showMessageDialog(null, "Update Selesai");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_txt_ubahActionPerformed

    private void txt_keywordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_keywordKeyReleased
        // memfungsikan textfield cari
        DefaultTableModel table = (DefaultTableModel) tbl_user.getModel();
        String search = txt_keyword.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
        tbl_user.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_txt_keywordKeyReleased

