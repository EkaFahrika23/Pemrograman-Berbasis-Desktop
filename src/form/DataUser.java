    private void tbl_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_userMouseClicked
        // mengaktifkan click mouse pada tabel
        int i = tbl_user.getSelectedRow();
        TableModel model = tbl_user.getModel();
        txt_id_user.setText(model.getValueAt(i, 0).toString());
        txt_username.setText(model.getValueAt(i, 1).toString());
        txt_password.setText(model.getValueAt(i, 2).toString());
        String comboSelect = model.getValueAt(i, 3).toString();
        switch (comboSelect) {
            case "Manager":
                cb_role.setSelectedIndex(0);
                break;
            case "Karyawan":
                cb_role.setSelectedIndex(1);
                break;

        }

    }//GEN-LAST:event_tbl_userMouseClicked

    private void tambahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBtnActionPerformed
        // Memfungsikan button tambah
        try {
            Connection con = KoneksiHelper.getConnection();
            //query insert data ke dalam database mysql
            pst = con.prepareStatement("INSERT INTO user (id_user, username, password, role) VALUES(?,?,?,?)");
            //validasi ketika textField kosong

            if (txt_id_user.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Id tidak boleh kosong");
            } else if (txt_username.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "username barang tidak boleh kosong");
            } else if (txt_password.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "password tidak boleh kosong");
                //digunakan untuk memasukkan data ke masing2 variabel textfield

            } else {

                //misal bingung pst sama rs bisa diliat di variabel diatas
                pst.setString(1, txt_id_user.getText());
                pst.setString(2, txt_username.getText());
                pst.setString(3, txt_password.getText());
                pst.setString(4, cb_role.getSelectedItem().toString());
                pst.executeUpdate();

                //setelah nginput data kasih ini biar textfieldnya kosong lagi
                txt_id_user.setText("");
                txt_username.setText("");
                txt_password.setText("");
                cb_role.setSelectedItem("");

                //setelah berhasil
                JOptionPane.showMessageDialog(null, "Tambah User Berhasil");
            }

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tambahBtnActionPerformed

